// 当前语言
let currentLang = 'zh-CN';

// 设置语言
function setLanguage(lang) {
  if (!translations[lang]) {
    console.warn('Language not supported:', lang);
    lang = 'zh-CN';
  }
  
  currentLang = lang;
  localStorage.setItem('preferred_lang', lang);
  updatePageLanguage();
}

// 更新页面语言
function updatePageLanguage() {
  const t = translations[currentLang];
  if (!t) return;
  
  // 更新所有带 data-i18n 属性的元素
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });
  
  // 更新 data-i18n-btn 属性
  document.querySelectorAll('[data-i18n-btn]').forEach(el => {
    const key = el.getAttribute('data-i18n-btn');
    if (t[key]) {
      el.textContent = t[key];
    }
  });
  
  // 更新 data-i18n-placeholder 属性
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) {
      el.placeholder = t[key];
    }
  });
  
  // 更新 title
  if (t.title) {
    document.title = t.title;
  }
  
  // 更新 html lang 属性
  document.documentElement.lang = currentLang;
  
  // 更新语言选择器
  const langSelect = document.getElementById('languageSelector');
  if (langSelect) {
    langSelect.value = currentLang;
  }
}

// 初始化语言
function initLanguage() {
  // 尝试从 localStorage 获取
  const saved = localStorage.getItem('preferred_lang');
  if (saved && translations[saved]) {
    currentLang = saved;
  } else {
    // 检测浏览器语言
    const browserLang = navigator.language || navigator.userLanguage;
    const supported = ['zh-CN', 'en-US', 'ko-KR', 'ja-JP', 'fr-FR'];
    
    for (const lang of supported) {
      if (browserLang.startsWith(lang.split('-')[0])) {
        currentLang = lang;
        break;
      }
    }
  }
  
  updatePageLanguage();
}

// 页面加载时初始化
if (typeof document !== 'undefined') {
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLanguage);
  } else {
    initLanguage();
  }
}

// 导出
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { translations, setLanguage, currentLang };
}
